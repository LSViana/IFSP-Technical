public class Exercise5 {
    
    // Quais são as principais diferenças entre as interfaces List, Set e Queue?
    
    // A interface List é voltada para o agrupamento de classes que são capazes de listar objetos, onde o usuário pode precisar saber onde está cada elemento e manter a ordem deles. A interface Set é semelhante à List, porém, não é voltada para estruturas que necessitam manter ordem de elementos. A interface Queue é voltada para estruturas que desejam implementar FIFO (First In, First Out).
    
}
